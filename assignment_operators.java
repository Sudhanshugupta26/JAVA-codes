public class assignment_operators {
	public static void main(String[] args) {
		int p =20 , q=10 , r=2 ;
		System.out.println(r);
		System.out.println(p+=q);
		System.out.println(p-=q);
		System.out.println(p/=q);
		System.out.println(p*=q);
		System.out.println(p%=q);
}
}